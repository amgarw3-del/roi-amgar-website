/**
 * מחזיר prompt מלא בעברית ליצירת תמונת אירוע.
 * שולח לPollinations/Flux (ולעתיד DALL-E 3 או Gemini).
 * הכיתוב (שם הפרשה + "הרב רועי אמגר") מוסף מאוחר יותר ע"י image-overlay.ts.
 */
export function buildEventImagePrompt(params: {
  eventName: string;
  eventKey?: string;
  group: EventGroup;
}): string {
  const { eventKey, group } = params;

  const config =
    (eventKey ? EVENT_MAP[eventKey] : undefined) ||
    GROUP_FALLBACK[group];

  // הסמל הראשי — הפריט הראשון לפני הפסיק
  const mainSymbol = config.symbolHe.split(",")[0].trim();

  return `צור תמונה ריבועית 1:1 של אמנות יהודית מקודשת.

חוקים מוחלטים — אסור בהחלט:
- אנשים, פנים, ידיים, גוף אנושי מכל סוג
- טקסט, אותיות, כתב, קליגרפיה, כיתובים מכל סוג

עיצוב:
- סגנון: אמנות יהודית קלאסית, אלגנטית ומינימליסטית
- רקע: גרדיאנט כהה וחלק (${config.paletteHe}) — רגוע ולא עמוס
- מרכז: סמל אחד בלבד — ${mainSymbol}
  זוהר רך ואטמוספרי סביב הסמל, עיבוד ברמת מוזיאון
- מסגרת: קו מסגרת זהב דק ואלגנטי
- מצב רוח: קדוש, נצחי, מרומם

פורמט: תמונה ריבועית 1:1, 1024×1024 פיקסלים.`;
}

/**
 * מחזיר event key לcache (slug + שנה עברית).
 */
export function buildEventCacheKey(eventKey: string, hebrewYear: string): string {
  return `${eventKey}-${hebrewYear}-v7`.replace(/[^a-z0-9-]/gi, "-");
}
